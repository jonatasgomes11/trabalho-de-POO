package Trabalho.java;

public class Quest1 {
	//String primeiraQuestão;

  // (A) Encapsulamento;
	
    /*Encapsulamento é um princípo de design de código, geralmente ligado a
	programação orientada, que nos orienta a esconder as funcionalidades e 
	funcionamento do nosso código dentro de pequenas 
    unidades (normalmente métodos e funções). Isso possibilita que
	modificações no sistema possam ser feitas de maneira mais cirurgicas, sem
     que uma funcionalidade esteja espalhada por diversas partes do sistema. */ 
	
	
  //  (B)  Herança;
	
	/*A Herança possibilita que as classes compartilhem seus atributos, métodos 
	e outros membros da classe entre si. Para a ligação entre as classes, a herança
	adota um relacionamento esquematizado hierarquicamente.*/
	
	/*O fato de as classes derivadas herdarem atributos das classes bases assegura
	que programas orientados a objetos cresçam de forma linear e não
	geometricamente em complexidade. Cada nova classe derivada não possui 
	interações imprevisíveis em relação ao restante do código do sistema.*/
	
	
  // (C)  Polimorfiamo;
	
	/*Definimos Polimorfismo como um princípio a partir do qual as classes
	derivadas de uma única classe base são capazes de invocar os 
	métodos que, embora apresentem a mesma assinatura, comportam-se de maneira
    diferente para cada uma das classes derivadas.*/
	
	/*O Polimorfismo é um mecanismo por meio do qual selecionamos as funcionalidades
	utilizadas de forma dinâmica por um programa no decorrer de sua execução.*/
 
	
  // (D)  Atributos e métodos;
	 
	/*Atributos são as propriedades de um objeto. Métodos são as ações que um
	objeto pode realizar. Os objetos são características definidas pelas classes. 
	Neles é permitido instanciar objetos da classe para inicializar os atributos e
	invocar os métodos.*/
   
	
  //  (E)  Atributos e métodos Estaticos;
	
	  // Atributos
	
	/*Atributo estático é um estado que vive na classe. Por conta disso, sempre que 
	você cria um novo objeto dessa classe, eles possuem o mesmo valor para o atributo. 
	Geralmente é usado para constantes.*/
	
	  // Metodos
	
	/*Os métodos static ou métodos da classe são funções que não dependem de nenhuma 
	variável de instância, quando invocados executam uma função sem a dependência do 
	conteúdo de um objeto ou a execução da instância de uma classe, conseguindo 
	chamar direto qualquer método da classe e também manipulando alguns campos 
	da classe.*/

}    

